public void populatedJtabledWithUser() {
ArrayList<MyClass.Users> userlist = user.UsersList();
String[] colNames = { "ID", "F-NAME", "L-NAME", "Username", "Password", "Type" };
Object[][] rows = new Object[userlist.size()][colNames.length];

for (int a = 0; a < userlist.size(); a++) {
    rows[a][0] = userlist.get(a).getId();
    rows[a][1] = userlist.get(a).getFirstname();
    rows[a][2] = userlist.get(a).getLastname();
    rows[a][3] = userlist.get(a).getUsername();
    rows[a][4] = userlist.get(a).getPassword();
    rows[a][5] = userlist.get(a).getUserType();
}
DefaultTableModel model = new DefaultTableModel(rows, colNames);
jTable_users.setModel(model);
        
}



   
