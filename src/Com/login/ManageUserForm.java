    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        String fname=jTextField_firstname_.getText();
        String lname=jTextField_lastname.getText();
        String username=jTextField_username.getText();
        String password1=String.valueOf(jPasswordField_1.getPassword());
        String password2=String.valueOf(jPasswordField_2.getPassword());
        String userType="user";
        if(jCheckBox_setAdmin.isSelected()){  userType="admin";    }
    
        if (fname.trim().isEmpty()){
           jLabel_emptyfirst.setForeground(Color.red);
          
        }else if (lname.trim().isEmpty()){
          jLabel_emptylast.setForeground(Color.red);
        }
        else if (username.trim().isEmpty()){
         // jLabel_emptypassword.setVisible(true);
         jLabel_emptyuser1.setForeground(Color.red);
        }
        else if (password1.trim().isEmpty()){
          //jLabel_emptypassword.setVisible(true);
          jLabel_emptypassword.setForeground(Color.red);
        }
        else if (!password1.equals(password2)){
          //jLabel_emptypassword.setVisible(true);
          JOptionPane.showMessageDialog(null, "Retype the correct password " ,"password error", 0);
        }
        else if(user.checkUsernameExists(0,username)){
            JOptionPane.showMessageDialog(null, "This username is already exists Try another one " ,"Username error", 0); 
        }
        else{
           
          user.addUsers(fname,lname,username,password1,userType);
          populatedJtabledWithUser();
           jTextField_id.setText("");
                   jTextField_firstname_.setText("");
                   jTextField_lastname.setText("");
                   jTextField_username.setText("");
                  jPasswordField_1.setText("");
                  jPasswordField_2.setText("");
                   jCheckBox_setAdmin.setSelected(false);
        }
    }//GEN-LAST:event_jButton_addActionPerformed

    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        
        String fname=jTextField_firstname_.getText();
        String lname=jTextField_lastname.getText();
        String username=jTextField_username.getText();
       String password1=String.valueOf(jPasswordField_1.getPassword());
       String password2=String.valueOf(jPasswordField_2.getPassword());
       String userType ="user";
       if (jCheckBox_setAdmin.isSelected()){
           userType ="admin";
       }
        
         if (fname.trim().isEmpty()){
           jLabel_emptyfirst.setForeground(Color.red);;
          
        }else if (lname.trim().isEmpty()){
          jLabel_emptylast.setForeground(Color.red);
        } else if (username.trim().isEmpty()){
         // jLabel_emptypassword.setVisible(true);
         jLabel_emptyuser1.setForeground(Color.red);
        }
        else if (password1.trim().isEmpty()){
          //jLabel_emptypassword.setVisible(true);
          jLabel_emptypassword.setForeground(Color.red);
        }
        else if (!password1.equals(password2)){
          //jLabel_emptypassword.setVisible(true);
          JOptionPane.showMessageDialog(null, "Retype the correct password " ,"password error", 0);
        }
        
        else{
            try{
                int id=Integer.parseInt(jTextField_id.getText());
               if(user.checkUsernameExists(id,username)){
            JOptionPane.showMessageDialog(null, "This username is already exists Try another one " ,"Username error", 0); 
                }
               else{
             user.editUsers(id,fname,lname,username,password1,userType); 
             populatedJtabledWithUser();
              jTextField_id.setText("");
                   jTextField_firstname_.setText("");
                   jTextField_lastname.setText("");
                   jTextField_username.setText("");
                  jPasswordField_1.setText("");
                  jPasswordField_2.setText("");
                   jCheckBox_setAdmin.setSelected(false);
               }
            }
           catch(NumberFormatException ex){
             JOptionPane.showMessageDialog(null, "Select user you want to edit from table " ,"No id selected", 0);  
           }
          
        }
    }//GEN-LAST:event_jButton_editActionPerformed

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
         try{
                   int id =Integer.valueOf(jTextField_id.getText());
                     int confirm = JOptionPane.showConfirmDialog(null,"Are you sure you want to delete this Users ?","confirmation Box",JOptionPane.YES_NO_OPTION);
               if(confirm==JOptionPane.YES_OPTION){
                     user.removeUsers(id);
                    }
                    
                    populatedJtabledWithUser();
                   jTextField_id.setText("");
                   jTextField_firstname_.setText("");
                   jTextField_lastname.setText("");
                   jTextField_username.setText("");
                  jPasswordField_1.setText("");
                  jPasswordField_2.setText("");
                   jCheckBox_setAdmin.setSelected(false);
              }
           catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "invalid User id"+ex.getMessage(), "error", 0);
           }
    }//GEN-LAST:event_jButton_deleteActionPerformed

